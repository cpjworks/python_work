# 5-7. Favorite Fruit: Make a list of your favorite fruits, 
# and then write a series of independent if statements that 
# check for certain fruits in your list.

fruits = ["bananas", "grapes", "pineapples", "strawberries", "blueberries"]

if "bananas" in fruits:
    print("We got 'nanas!")
if "grapes" in fruits:
    print("Crispy grapes, right here!")
if "pineapples" in fruits:
    print("Someone order spiky and sweet?")
if "strawberries" in fruits:
    print("Who knows how to make jam?")
if "blueberries" in fruits:
    print("Where is the porridge to add these to?")



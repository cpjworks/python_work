# Sorting lists in Python

cars = ["bmw", "toyota", "audi", "subaru"]
print("Here is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

print("\nHere is the reverse sorted list:")
cars.reverse()
print(cars)

len(cars)
